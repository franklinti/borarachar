package br.com.borarachar.model

class Calculadora {
    fun soma(a: Int , b:Int): Int {
        return a + b
    }
    fun subtracaco(a: Int , b: Int): Int{
        return a - b
    }fun multiplicacao(a: Int , b: Int): Int{
        return a * b
    }
    fun divisao(a: Int , b: Int): Int{
        return a / b
    }
}